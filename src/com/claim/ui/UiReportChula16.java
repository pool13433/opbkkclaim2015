    private void comboYearCenter16JulaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboYearCenter16JulaItemStateChanged
        comboNumCenter16Jula.removeAllItems();
        comboNumCenter16Jula.addItem("-");
        for (Stmp stmp : listStmpCenter16_jula) {
            //System.out.println("comboYearOpaeIndv.getSelectedItem()++"+comboYearOpaeIndv.getSelectedItem());
            //System.out.println("stmp.getStmpYM()++"+stmp.getStmpYM());
            if (comboYearCenter16Jula.getSelectedItem().equals(stmp.getStmpYM())
                    && comboNumCenter16Jula.getSelectedItem() != stmp.getStmpN()) {
                comboNumCenter16Jula.addItem(stmp.getStmpN());
            }
        }
    }//GEN-LAST:event_comboYearCenter16JulaItemStateChanged

