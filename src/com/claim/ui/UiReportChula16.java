    private void btnCenter16JularGenReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCenter16JularGenReportActionPerformed
        final DialogWaitProcess waitArea = new DialogWaitProcess();
        try {
            if (comboYearCenter16Jula.getSelectedIndex() != 0) {
                if (comboNumCenter16Jula.getSelectedIndex() != 0) {
                    if (radioCondition16.isSelected() || radioConditionJula.isSelected()) {
                        if (!txtPathFileCenter16Jula.getText().isEmpty()) {
                            int selectOption = JOptionPane.showConfirmDialog(null,
                                    ConstantMessage.MSG_REPORT_CONFIRM,
                                    ConstantMessage.MSG_REPORT_INFOMATION, JOptionPane.YES_NO_OPTION);
                            if (selectOption == JOptionPane.YES_OPTION) {
                                waitArea.setVisible(true);
                                Thread thread = new Thread(new Runnable() {
                                    @Override
                                    public void run() {
                                        DateUtil dateUtil = new DateUtil();
                                        ProgrameStatus programeStatus = new ProgrameStatus();
                                        try {
                                            OppReport report = setInputFormReport();
                                            waitArea.setTitle(ConstantMessage.MSG_PROGRAME_PROCESSSING);
                                            boolean is2015 = dateUtil.checkStmpBudgetYear2015(report.getStmp(), MAX_MONTH_BUDGET);
                                            if (is2015) {
                                                programeStatus = new Center16AndJula2015Controller().center16_Jula_2015(report);
                                            } else {
                                                programeStatus = center16AndJulaController.center16_Jula(report);
                                            }
                                            waitArea.setVisible(false);
                                            baseFunction.openDirectoryAutomatic(report.getPathFile());
                                            /*
                                            * 17-08-2015
                                            */
                                            /*try {                                                
                                                new LogAppController().createLog("ทำการออกรายงาน", "ทำการออกรายงาน ศูนย์ 16 จุฬา ", NetUtils.getIpAddress(), 1);
                                            } catch (UnknownHostException ex) {
                                                Logger.getLogger(UiMainClaim.class.getName()).log(Level.SEVERE, null, ex);
                                            }*/
                                        } catch (Exception e) {
                                            e.printStackTrace();
                                        }
                                    }
                                });
                                thread.start();
                            }
                        } else {
                            JOptionPane.showMessageDialog(this,
                                    ConstantMessage.MSG_FILE_LOCATION_PLEASE,
                                    ConstantMessage.MSG_FILE_LOCATION,
                                    JOptionPane.ERROR_MESSAGE);
                        }
                    } else {
                        JOptionPane.showMessageDialog(this,
                                "กรุณาเลือก เงื่อนไขที่ออก",
                                ConstantMessage.MSG_SELECT_DATA,
                                JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(this,
                            "กรุณาเลือกงวดที่",
                            ConstantMessage.MSG_SELECT_DATA,
                            JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this,
                        ConstantMessage.MSG_SELECT_DATE_PLEASE,
                        ConstantMessage.MSG_SELECT_DATA,
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnCenter16JularGenReportActionPerformed

