    public void loadCombo() {
        try {
            //Tab Center 16 _jula
            //get Stmp From Table RPT_OPBKK_HC16CHULA
            comboYearCenter16Jula.removeAllItems();
            comboYearCenter16Jula.addItem("-");
            listStmpCenter16_jula = new UtilDao().getStmpDistinct("RPT_OPBKK_HC16CHULA", "STMP");

            List<String> listCenter16_jula = new ArrayList<String>();

            for (int i = 0; i < listStmpCenter16_jula.size(); i++) {
                listCenter16_jula.add(listStmpCenter16_jula.get(i).getStmpYM());
            }
            List<String> listUnDup =  ArrayListUtil.removeDuplicates(listCenter16_jula);            
            Collections.sort(listUnDup);
            for (String string : listUnDup) {
                comboYearCenter16Jula.addItem(string);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    public OppReport setInputFormReport() {
        OppReport report = new OppReport();
        report.setYearMonth(comboYearCenter16Jula.getSelectedItem().toString());
        report.setNo(comboNumCenter16Jula.getSelectedItem().toString());
        report.setPathFile(txtPathFileCenter16Jula.getText());
        report.setMonth(baseFunction.subStringYearMonth(comboYearCenter16Jula.getSelectedItem().toString()).get("M").toString());
        report.setYear(baseFunction.subStringYearMonth(comboYearCenter16Jula.getSelectedItem().toString()).get("Y").toString());
        report.setStmp(report.getYear() + report.getMonth() + "-" + report.getNo());
        if (radioCondition16.isSelected()) {
            report.setServiceCode("13661");
        } else if (radioConditionJula.isSelected()) {
            report.setServiceCode("13756");
        }
        return report;
    }
