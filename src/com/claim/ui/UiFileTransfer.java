    private void loadTableDataModel() {
        List<ObjFileTransfer> ftpList = ftpController.getFileTransfer(ConstantFtp.FTP_RPT_AlIASES[comboReportType.getSelectedIndex()]);
        ((DefaultTableModel) table_files_server.getModel()).setRowCount(0);
        for (int i = 0; i < ftpList.size(); i++) {
            int auto = (i + 1);
            ObjFileTransfer objLog = ftpList.get(i);
            Vector data = new Vector();
            data.add(String.valueOf(auto));
            data.add(objLog.getFtp_name());
            data.add(objLog.getFtp_desc());
            data.add(objLog.getFtp_stmp());
            data.add(objLog.getFtp_createdate());
            data.add(ConstantFtp.FTP_STATUS[objLog.getFtp_status()]);
            model.addRow(data);
            table_files_server.setRowHeight(i, 30);
        }
        table_files_server.setModel(model);
        
        new HeaderRenderer().setWidthAsPercentages(table_files_server, 0.05, 0.20, 0.25, 0.10, 0.20, 0.20);
    }
    
    private void loadCombo() {
        comboReportType.removeAllItems();
        String[] ftpReportType = ConstantFtp.FTP_RPT_LABEL;
        for (String ftpRpt : ftpReportType) {
            comboReportType.addItem(ftpRpt);
        }
    }

