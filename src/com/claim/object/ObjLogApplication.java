package com.claim.object;

import com.claim.support.StringOpUtil;

/**
 *
 * @author Poolsawat.a
 */
public class ObjLogApplication {

    private int log_id;
    private String log_name;
    private String log_desc;
    private String log_ip;
    private String result;
    private String stmp;
    private int record;
    private String log_createdate;
    private int log_status;
    private String log_status_desc;

    public int getLog_id() {
        return log_id;
    }

    public void setLog_id(int log_id) {
        this.log_id = log_id;
    }

    public String getLog_name() {
        return StringOpUtil.removeNull(log_name);
    }

    public void setLog_name(String log_name) {
        this.log_name = log_name;
    }

    public String getLog_desc() {
        return StringOpUtil.removeNull(log_desc);
    }

    public void setLog_desc(String log_desc) {
        this.log_desc = log_desc;
    }

    public String getLog_ip() {
        return StringOpUtil.removeNull(log_ip);
    }

    public void setLog_ip(String log_ip) {
        this.log_ip = log_ip;
    }

    public String getLog_createdate() {
        return StringOpUtil.removeNull(log_createdate);
    }

    public void setLog_createdate(String log_createdate) {
        this.log_createdate = log_createdate;
    }

    public int getLog_status() {
        return log_status;
    }

    public void setLog_status(int log_status) {
        this.log_status = log_status;
    }

    public String getLog_status_desc() {
        return StringOpUtil.removeNull(log_status_desc);
    }

    public void setLog_status_desc(String log_status_desc) {
        this.log_status_desc = log_status_desc;
    }

    public String getResult() {
        return StringOpUtil.removeNull(result);
    }

    public void setResult(String result) {
        this.result = result;
    }

    public int getRecord() {
        return record;
    }

    public void setRecord(int record) {
        this.record = record;
    }

    public String getStmp() {
        return StringOpUtil.removeNull(stmp);
    }

    public void setStmp(String stmp) {
        this.stmp = stmp;
    }
    
}
